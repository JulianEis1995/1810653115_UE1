public class Aufgabe4_1810653115
{
    public static void main(String[] args)
    {
        //Erstellen von Variablen vom Typ Integer
        int zahl1 = 5;
        int zahl2 = 2;

        /* kleiner, größer, kleiner oder gleich, größer oder gleich
            gleich, ungleich */
        System.out.println(zahl1 < zahl2);
        System.out.println(zahl2 < zahl1);
        System.out.println(zahl1 > zahl2);
        System.out.println(zahl2 > zahl1);
        System.out.println(zahl1 <= zahl2);
        System.out.println(zahl2 <= zahl1);
        System.out.println(zahl1 >= zahl2);
        System.out.println(zahl2 >= zahl1);
        System.out.println(zahl1 == zahl2);
        System.out.println(zahl1 != zahl2);

    }
}
